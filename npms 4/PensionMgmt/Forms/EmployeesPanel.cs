            parent.Controls.Add(l);
            parent.Controls.Add(txt);
        }

        private void LoadGrid(string search = "")
        {
            DataTable dt;
            string sql;
            if (string.IsNullOrEmpty(search))
            {
                sql = "SELECT EmployeeId, FullName, Department, Rank, BasicSalary, DateOfBirth, JoiningDate, Status FROM Employees ORDER BY FullName";
                dt = _db.GetTable(sql);
            }
            else
            {
                sql = "SELECT EmployeeId, FullName, Department, Rank, BasicSalary, DateOfBirth, JoiningDate, Status " +
                      "FROM Employees WHERE EmployeeId LIKE @s OR FullName LIKE @s ORDER BY FullName";
                dt = _db.GetTable(sql, new SqlParameter("@s", "%" + search + "%"));
            }
            dgv.DataSource = dt;
        }

        private void ShowForm(bool editMode)
        {
            _isEditing = editMode;
            pnlForm.Visible = true;
            if (!editMode)
            {
                // Clear all fields for a fresh add — no Employee ID field to clear
                txtName.Clear();
                txtDept.Clear();
                txtSalary.Clear();
                cmbRank.SelectedIndex = 4;
                cmbStatus.SelectedIndex = 0;
                dtpBirth.Value = DateTime.Today.AddYears(-30);
                dtpJoining.Value = DateTime.Today;
                _editingId = null;
            }
        }

        private void Dgv_DoubleClick(object sender, EventArgs e)
        {
            if (!_readOnly) EditSelected();
        }

        private void EditSelected()
        {
            if (dgv.CurrentRow == null) return;
            DataRow row = ((DataTable)dgv.DataSource).Rows[dgv.CurrentRow.Index];
            _editingId = row["EmployeeId"].ToString();
            txtName.Text = row["FullName"].ToString();
            txtDept.Text = row["Department"].ToString();
            txtSalary.Text = row["BasicSalary"].ToString();
            cmbRank.Text = row["Rank"].ToString();
            cmbStatus.Text = row["Status"].ToString();
            dtpBirth.Value = Convert.ToDateTime(row["DateOfBirth"]);
            dtpJoining.Value = Convert.ToDateTime(row["JoiningDate"]);
            ShowForm(editMode: true);
        }

        private void DeleteSelected()
        {
            if (dgv.CurrentRow == null) { MessageBox.Show("Select an employee first."); return; }
            DataRow row = ((DataTable)dgv.DataSource).Rows[dgv.CurrentRow.Index];
            string id = row["EmployeeId"].ToString();
            string name = row["FullName"].ToString();

            if (MessageBox.Show($"Delete {name} ({id})?", "Confirm Delete",
                    MessageBoxButtons.YesNo, MessageBoxIcon.Warning) == DialogResult.Yes)
            {
                int n = _db.Execute("DELETE FROM Employees WHERE EmployeeId=@id", new SqlParameter("@id", id));
                if (n > 0)
                {
                    AuditLogger.Log("Employee Deleted", id, name,
                        string.Format("Employee '{0}' (ID: {1}) was deleted from the system.", name, id));
                    MessageBox.Show("Employee deleted.");
                    LoadGrid();
                }
            }
        }

        private void BtnSave_Click(object sender, EventArgs e)
        {
            // Validate fields (no Employee ID field anymore)
            if (string.IsNullOrWhiteSpace(txtName.Text) ||
                string.IsNullOrWhiteSpace(txtDept.Text) ||
                string.IsNullOrWhiteSpace(txtSalary.Text))
            {
                MessageBox.Show("Please fill all required fields.", "Validation",
                    MessageBoxButtons.OK, MessageBoxIcon.Warning);
                return;
            }

            if (!decimal.TryParse(txtSalary.Text, out decimal salary) || salary <= 0)
            {
                MessageBox.Show("Enter a valid Basic Salary.", "Validation",
                    MessageBoxButtons.OK, MessageBoxIcon.Warning);
                return;
            }

            if (dtpBirth.Value >= dtpJoining.Value)
            {
                MessageBox.Show("Joining Date must be after Date of Birth.", "Validation",
                    MessageBoxButtons.OK, MessageBoxIcon.Warning);
                return;
            }

            string name = txtName.Text.Trim();
            string dept = txtDept.Text.Trim();
            string rank = cmbRank.Text;
            string status = cmbStatus.Text;
            DateTime dob = dtpBirth.Value.Date;
            DateTime doj = dtpJoining.Value.Date;

            if (_isEditing)
            {
                int n = _db.Execute(
                    "UPDATE Employees SET FullName=@n, Department=@d, Rank=@r, " +
                    "BasicSalary=@s, DateOfBirth=@b, JoiningDate=@j, Status=@st " +
                    "WHERE EmployeeId=@id",
                    new SqlParameter("@n", name),
                    new SqlParameter("@d", dept),
                    new SqlParameter("@r", rank),
                    new SqlParameter("@s", salary),
                    new SqlParameter("@b", dob),
                    new SqlParameter("@j", doj),
                    new SqlParameter("@st", status),
                    new SqlParameter("@id", _editingId));

                if (n > 0)
                {
                    AuditLogger.Log("Employee Updated", _editingId, name,
                        string.Format("Employee '{0}' (ID: {1}) updated. Dept: {2}, Rank: {3}, Salary: {4:N0}, Status: {5}.",
                            name, _editingId, dept, rank, salary, status));
                    MessageBox.Show("Employee updated successfully.");
                    pnlForm.Visible = false;
                    LoadGrid();
                }
            }
            else
            {
                // INSERT without EmployeeId — database computes it automatically
                _db.Execute(
                    "INSERT INTO Employees (FullName, Department, Rank, BasicSalary, DateOfBirth, JoiningDate, Status) " +
                    "VALUES (@n, @d, @r, @s, @b, @j, @st)",
                    new SqlParameter("@n", name),
                    new SqlParameter("@d", dept),
                    new SqlParameter("@r", rank),
                    new SqlParameter("@s", salary),
                    new SqlParameter("@b", dob),
                    new SqlParameter("@j", doj),
                    new SqlParameter("@st", status));

                // Read back the auto-generated EmployeeId
                object result = _db.ExecuteScalar(
                    "SELECT TOP 1 EmployeeId FROM Employees ORDER BY Id DESC");
                string newEmpId = result?.ToString() ?? "N/A";

                AuditLogger.Log("Employee Added", newEmpId, name,
                    string.Format("New employee '{0}' (ID: {1}) added. Dept: {2}, Rank: {3}, Salary: {4:N0}, Status: {5}.",
                        name, newEmpId, dept, rank, salary, status));

                MessageBox.Show(string.Format("Employee added successfully.\nAssigned Employee ID: {0}", newEmpId));
                pnlForm.Visible = false;
                LoadGrid();

                if (status == "Retired")
                    TryAutoCreatePendingPayout(newEmpId, name, salary, dob, doj, rank);
            }
        }

        private void TryAutoCreatePendingPayout(
            string empId, string name, decimal salary,
            DateTime dob, DateTime doj, string rank)
        {
            bool eligible = PensionCalculator.Calculate(
                salary, dob, doj, rank,
                out decimal monthly, out decimal lump, out string msg);

            if (!eligible) return;

            object existingCount = _db.ExecuteScalar(
                "SELECT COUNT(*) FROM Payouts WHERE EmployeeId = @id",
                new SqlParameter("@id", empId));
            if (Convert.ToInt32(existingCount) > 0) return;

            _db.Execute(
                "INSERT INTO Payouts (EmployeeId, FullName, MonthlyPension, LumpSum, ProcessedDate, Notes, Status) " +
                "VALUES (@id, @name, @m, @l, @d, @notes, 'Pending')",
                new SqlParameter("@id", empId),
                new SqlParameter("@name", name),
                new SqlParameter("@m", monthly),
                new SqlParameter("@l", lump),
                new SqlParameter("@d", DateTime.Today),
                new SqlParameter("@notes", "Auto-generated on employee registration as Retired."));

            AuditLogger.Log("Payout Pending", empId, name,
                string.Format("Auto-generated Pending payout for retired employee '{0}'. Monthly: BDT {1:N2}, Lump Sum: BDT {2:N2}.",
                    name, monthly, lump));

            MessageBox.Show(
                string.Format(
                    "Employee '{0}' is eligible for pension and is marked as Retired.\n\n" +
                    "Monthly Pension : BDT {1:N2}\n" +
                    "Lump Sum        : BDT {2:N2}\n\n" +
                    "A Pending payout has been automatically created.\n" +
                    "Go to Pension Payouts to approve it.",
                    name, monthly, lump),
                "Auto Payout Created", MessageBoxButtons.OK, MessageBoxIcon.Information);
        }
