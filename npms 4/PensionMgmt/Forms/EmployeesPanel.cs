        private void AddFormRow(Panel parent, string label, int x, int y, int lw, out TextBox txt)
        {
            Label l = new Label { Text = label, Bounds = new Rectangle(x, y, lw, 28), TextAlign = ContentAlignment.MiddleLeft };
